a = 5
b = 10

def add():
    added_value = a + b
    print(added_value)

def subtract():
    subtracted_value = b - a
    print(subtracted_value)

def multiply():
    multiplied_value = a * b
    print(multiplied_value)

def divide():
    divided_value = b / a
    print(divided_value)

def modulus():
    modulus_value = b % a
    print(modulus_value)

add()
subtract()
multiply()
divide()